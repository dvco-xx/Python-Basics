"""integer type"""
a=13
b=100
c=26
'''floored quotient'''
i=b//c
print(a,b,c)
print(i)
print(type(a))

"""float type"""
x=33.5
y=-25.8
z=205.0
print(x,y,z)
print(abs(z))
print(type(z))

"""complex type"""
d=3+5j
print(d)
print(type(d))

"""binary type"""
e=0B1010
print(e)
print(type(e))

"""hexadecimal"""
f=0x4A
print(f)
print(type(f))

"""octal type"""
g=0o45
print(g)
print(type(g))

"""boolean"""
h=True
print(h)
print(type(h))
print(7<6)

j=int(x)
print(int(y))
print(type(j))

k=int("22")
print(float(k))

print(bin(11))

